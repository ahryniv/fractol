#include "libft.h"

void	ft_putnbr(int nb)
{
	int		b;
	char	c;

	b = nb;
	if (b == (-2147483648))
		nb = (-214748364);
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		c = nb % 10 + '0';
		write(1, &c, 1);
	}
	if (nb < 10)
	{
		c = nb + '0';
		write(1, &c, 1);
	}
	if (b == (-2147483648))
		write(1, "8", 1);
}
