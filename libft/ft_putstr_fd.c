#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	size_t	i;
	char	c;

	i = 0;
	if (s)
	{
		while (i < ft_strlen(s))
		{
			c = s[i];
			write(fd, &c, 1);
			i++;
		}
	}
}
