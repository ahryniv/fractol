#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*cc;

	cc = (char*)b;
	i = 0;
	while (i < len)
	{
		cc[i] = c;
		i++;
	}
	return (b);
}
